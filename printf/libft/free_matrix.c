#include "libft.h"

void	free_matrix(void **mat)
{
	int		i;
	char	**m;

	i = 0;
	m = (char **)mat;
	if (!mat)
		return ;
	while (m[i])
	{
		free(m[i]);
		m[i] = NULL;
		i++;
	}
	free(m);
	m = NULL;
}
