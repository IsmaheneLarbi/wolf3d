#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (src[i])
	{
		i++;
	}
	while (j < i + 1)
	{
		dst[j] = src[j];
		j++;
	}
	dst[i] = '\0';
	return (dst);
}
